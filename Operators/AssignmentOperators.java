package Operators;

public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 10;
        // a= a+10;
        a += 10;
        int b = 5;
        // b=b-2;
        b -= 2;
        int c = 5;
        // c = c*2;
        c *= 5;
        int d = 10;
        // d= d/2;
        d /= 2;
        int e = 10;
        // e = e%10;
        e %= 10;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }

}
